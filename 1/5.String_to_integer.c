#include <stdio.h>

#include <stdio.h>

int stringToInteger(const char *str) {
    int result = 0;
    int sign = 1;
    int i = 0;

    if (str[0] == '-') {
        sign = -1;
        i = 1;
    } else if (str[0] == '+') {
        i = 1;
    }

    while (str[i] >= '0' && str[i] <= '9') {
        result = result * 10 + (str[i] - '0');
        i++;
    }

    return sign * result;
}

int main() {
    char input[] = "12345"; 
    int in_num = stringToInteger(input);
    printf("String to integer is %d\n", in_num);
    return 0;
}



